#include <signal.h>
#include "minitalk.h"

unsigned int	ft_loadpid(const char *str)
{
	unsigned char	i;
	unsigned int	pid;

	i = 0;
	pid = 0;
	while ((i < 6) && ('0' <= str[i]) && (str[i] <= '9'))
		pid = 10 * pid + str[i++] - '0';
	if ((i == 6) || str[i])
		return (0);
	return (pid);
}
